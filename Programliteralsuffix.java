package charTypecasting;

public class Programliteralsuffix {

	public static void main(String[] args) {
		long a = 65;
		long a1 = 65l;
		long b = 3873476;
		long b1 = 8767543l;
		long c = 7493859398787L; // for long suffix l/L is necessary
		long d = 749385l;
		
		
		System.out.println(a);
		System.out.println(a1);
		System.out.println(b);
		System.out.println(b1);
		System.out.println(c);
		System.out.println(d);
		
		
	}

}
